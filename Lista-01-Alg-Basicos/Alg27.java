public class Alg27 {
    public static void main(String[] args) {
        int x, y;
        x = 2;
        y = 5;
        System.out.println("\nx = " + x);
        x++;
        System.out.println("\ny = " + y);
        y--;
        System.out.println("\nNovo valor de x = " + x);
        System.out.println("\nNovo valor de y = " + y + "\n");
    }
}